# Ciclo WHILE (mientras).
# Un ciclo es un BUCLE que se está repitiendo en base a ciertas condiciones lógicas
# cada repetición se llama ITERACIÓN

# Un ciclo while permite realizar una serie de acciones posteriores a la validación
# de la condición lógica

contador = 0

while contador <= 10:
    print(f'Valor contador: {contador}')
    # contador = contador + 1
    contador += 1